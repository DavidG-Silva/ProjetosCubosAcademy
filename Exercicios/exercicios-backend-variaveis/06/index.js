
let distanciaPercorrida = 500; // informar em metros
let tempo = 10; // informar em segundos

let velocidadeKMH; //resultara em um valor KM por horas

velocidadeKMH = (distanciaPercorrida / tempo) * 3.6;

console.log(`Ao percorrer ${distanciaPercorrida} metros em ${tempo} segundos, 
você fez o percurso à velocidade média de ${velocidadeKMH} km/h!`);
